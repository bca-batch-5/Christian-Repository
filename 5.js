//For
function loopFor(){
    for(let i = 10;  i <= 15; i++){
        console.log(i);
    }
}
// loopFor();

function loopWhile(){
    let i =10;
    while(i <= 15){
        console.log(i);
        i++;
    }
}
// loopWhile();

function loopDoWhile(){
    let i = 10;
    do{
        console.log(i);
        i++;
    }while(i <= 15);
}
loopDoWhile();